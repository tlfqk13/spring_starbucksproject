package com.scit.starbucks.mygradevo;

public class MyGradeVO 
{	
	private int myGradeID;
	private int starPointNum;
	private String starDate;
	private String userID;
	private int gradeID;
	
	public MyGradeVO() {
		super();
	}
	public int getMyGradeID() {
		return myGradeID;
	}
	public void setMyGradeID(int myGradeID) {
		this.myGradeID = myGradeID;
	}
	public int getStarPointNum() {
		return starPointNum;
	}
	public void setStarPointNum(int starPointNum) {
		this.starPointNum = starPointNum;
	}
	public String getStarDate() {
		return starDate;
	}
	public void setStarDate(String starDate) {
		this.starDate = starDate;
	}
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public int getGradeID() {
		return gradeID;
	}
	public void setGradeID(int gradeID) {
		this.gradeID = gradeID;
	}

	
	
	
	
}
